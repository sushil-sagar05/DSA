class Solution {
public:
    int binaryGap(int n) {
        int currBitPos=0;
        int prevBitPos=-1;
        int result=0;
        while(n>0){
            if((n&1)>0){
                result=(prevBitPos != -1 )? max(result,currBitPos-prevBitPos):result;
                prevBitPos=currBitPos;
            }
            currBitPos++;
            n>>=1;
        }
        return result;

    }
};